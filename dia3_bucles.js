// el bucle for sirve para repetir algo un número determinado de veces
for (let i = 1; i <= 5; i++) {
  console.log("Este es el número:", i);
}

// el bucle while sirve para repetir algo mientras una condición sea verdadera

let contador = 1;

while (contador <= 3) {
  console.log("Contando...", contador);
  contador++;
}

for (let i = 1; i <= 10; i++) {
  console.log(i);
}

for (let i = 1; i <= 10; i++) {
  if (i % 2 === 0) {
    console.log(i);
  }
}

let i = 5;

while (i > 0) {
  console.log(i);
  i--;
}
